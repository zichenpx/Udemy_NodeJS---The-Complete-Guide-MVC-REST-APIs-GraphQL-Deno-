exports.getCart = (req, res, next) => {
  req.user
    .populate('cart.items.productId')
    .execPopulate() //becasue populate() is not a promise function
    .then(user => {
      // console.log(user);
      // console.log(user.cart.items);
      const products = user.cart.items;
      res.render('shop/cart', {
        path: '/cart',
        pageTitle: 'Your Cart',
        products: products
      });
    })
    .catch(err => console.log(err));
};

exports.postCart = (req, res, next) => {
  const prodId = req.body.productId;
  Product.findById(prodId)
    .then(product => {
      return req.user.addToCart(product);
    })
    .then(result => {
      console.log(result);
      res.redirect('/cart');
    });
};

exports.postCartDeleteProduct = (req, res, next) => {
  const prodId = req.body.productId;
  req.user
    .removeFromCart(prodId)
    .then(result => {
      res.redirect('/cart');
    })
    .catch(err => console.log(err));
};

exports.postOrder = (req, res, next) => {
  req.user
    .populate('cart.items.productId')
    .execPopulate()
    .then(user => {
      // !product here is an array! An array of elements were 
      // we have the quantity and then the real product data 
      // was nested in a product ID field
      const products = user.cart.items.map(i => {
        return { quantity: i.quantity, product: { ...i.productId._doc } };
        // for us need to have a product field with the product data and a 
        //  quantity field, that is almost what we have but we have the 
        // product data in a product ID field here, not in a product field 
        // but that is easy to fix. I will simply not just extract items 
        // like this, I will also map the items so that I store the changed 
        // items in my products array and a mapped item should still have 
        // its quantity so I'll keep that, the i simply refers to the 
        // item since this function goes through all items in that array, 
        // so we'll have the quantity. And then we'll have a product field 
        // and the product field should have all the product data so that 
        // we'll store everything that I had in i product ID before because 
        // that was the old structure we had in there, now we have this structure,
      });
      const order = new Order({
        user: {
          name: req.user.name,
          userId: req.user
        },
        products: products
      });
      return order.save();
    })
    .then(result => {
      return req.user.clearCart();
    })
    .then(() => {
      res.redirect('/orders');
    })
    .catch(err => console.log(err));
};

